    public void cargarPuestos() {
        String[] puestos = ControllerSistema.getInstancia().getPuestosDisponibles();

        for(String s : puestos) {
            if(s != null) {
                lstPuestos.add(s);
            }
        }
        lstPuestos.select(0);
    }
